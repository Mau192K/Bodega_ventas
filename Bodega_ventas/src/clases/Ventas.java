    private void AddtokartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddtokartActionPerformed
        int fila=TbClientes.getSelectedRow();
        boolean nuevo=true;
        if (fila>=0){
        String codpro=TbClientes.getValueAt(fila,0).toString();
        String precio=TbClientes.getValueAt(fila,2).toString();
        String stocktotal=TbClientes.getValueAt(fila,3).toString();
        int Stockprod=Integer.parseInt(stocktotal);
        if(!ShowClientID.getText().equals("")){
           if(!Cantprod.getText().equals("")){
               int Stockcomp=Integer.parseInt(Cantprod.getText());
               if(Stockcomp!=0){
                   if(Stockcomp<=Stockprod){
                       try{
                           com.mysql.jdbc.Statement st2=(com.mysql.jdbc.Statement) cn.createStatement();
                           ResultSet rs2=st2.executeQuery("SELECT*FROM `ventas`");
                           while (rs2.next()) {
                           String data=rs2.getString(3);
                               if(codpro.equals(data)){
                                   nuevo=false;
                                   break;
                               }   
                           }
                           if (nuevo) {
                           java.sql.PreparedStatement pst= cn.prepareStatement("INSERT INTO ventas (IDUser,ClaveCli,ClavePro,Cantidad,Precio)values(?,?,?,?,?)");
                           pst.setString(1, ClaveUser.getText());
                           pst.setString(2, ShowClientID.getText());
                           pst.setString(3, codpro);
                           pst.setString(4, Cantprod.getText());
                           double precitotal,preciuni,canti;
                           canti=Double.parseDouble(Cantprod.getText());
                           preciuni=Double.parseDouble(precio);
                           precitotal=canti*preciuni;
                           pst.setString(5,Double.toString(precitotal));
                           pst.executeUpdate();
                           mostrardatos("","");
                           Claveproduct.setText("");
                           Cantprod.setText("");
                           }else{
                            JOptionPane.showMessageDialog(null, "No se pueden añadir dos veces el mismo productos");
                           }
                       }catch(Exception e){
                           System.out.println(e);
                       }
                   }else{
                    JOptionPane.showMessageDialog(null, "Ingrese una cantidad menor \n al inventario total");  
                   }
               }else{
                   Cantprod.setText("Cantidad inválida");
               }
           }else{
               Cantprod.setText("Ingrese la cantidad a comprar");
           }
        }else{
            NombreClient.setText("Ingrese un cliente");
        }
        }else{
            JOptionPane.showMessageDialog(null, "No se seleccionó fila");
        }

        
    }//GEN-LAST:event_AddtokartActionPerformed

