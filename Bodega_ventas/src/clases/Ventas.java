        public void mostrardatos(String categ,String nom){
        TbClientes.setDefaultRenderer(Object.class, new TablaImagen());
        DefaultTableModel modelo =new DefaultTableModel();
        modelo.addColumn("Clave Producto");
        modelo.addColumn("Descripción");
        modelo.addColumn("Precio");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Categoria");
        modelo.addColumn("Imagen");
        TbClientes.setModel(modelo);
        String sql="";
        if (categ.equals("")){
            sql="SELECT * FROM productos";
            
        }else{
            sql="SELECT * FROM productos WHERE Categ= '"+categ+"'";
        }
        if (!nom.equals("")){
            sql="SELECT * FROM productos WHERE Codpro= '"+nom+"'";
        } else {
        }
        try{
        Object [] datos=new Object [8];
        Statement st= cn.createStatement();
        ResultSet rs=st.executeQuery(sql);
        while(rs.next()){
            datos[0]=rs.getString(1);
            datos[1]=rs.getString(2);
            datos[2]=rs.getString(3);
            datos[3]=rs.getString(4);
            datos[4]=rs.getString(5);
            Blob blob = rs.getBlob(7);
            try{
            byte []data = blob.getBytes(1, (int)blob.length());
            BufferedImage image = null; 
            InputStream in=new ByteArrayInputStream(data);
            image= ImageIO.read(in);
            ImageIcon imgi= new ImageIcon(image.getScaledInstance(100, 100, 0));
            datos[5]= new JLabel(imgi);
            }catch(Exception ex){
                datos[7]="No Imagen";
            }
            modelo.addRow(datos);
        }
        TbClientes.setModel(modelo);
        TbClientes.setRowHeight(100);
        }catch (SQLException ex){
            Logger.getLogger(ProductList.class.getName()).log(Level.SEVERE,null,ex);
        }
    }

