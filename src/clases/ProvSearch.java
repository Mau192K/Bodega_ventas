    public void mostrardatos(String Valor){
        TbClientes.setDefaultRenderer(Object.class, new TablaImagen());
        DefaultTableModel modelo =new DefaultTableModel();
        modelo.addColumn("CLAVE");
        modelo.addColumn("Proveedor");
        modelo.addColumn("No. Contacto");
        modelo.addColumn("RUTA");
        modelo.addColumn("LOGO");
        TbClientes.setModel(modelo);
        String sql="SELECT * FROM proveedores WHERE NombreComp = '"+Valor+"'";
        try{
        Object [] datos=new Object [5];
        Statement st= cn.createStatement();
        ResultSet rs=st.executeQuery(sql);
        while(rs.next()){
            datos[0]=rs.getString(1);
            datos[1]=rs.getString(2);
            datos[2]=rs.getString(3);
            datos[3]=rs.getString(4);
            Blob blob = rs.getBlob(5);
            try{
            byte []data = blob.getBytes(1, (int)blob.length());
            BufferedImage image = null;
            InputStream in=new ByteArrayInputStream(data);
            image= ImageIO.read(in);
            ImageIcon imgi= new ImageIcon(image.getScaledInstance(100, 100, 0));
            datos[4]= new JLabel(imgi);
            }catch(Exception ex){
                datos[5]="No Imagen";
            }
            modelo.addRow(datos);
            
        }
        TbClientes.setModel(modelo);
        TbClientes.setRowHeight(100);
        }catch (SQLException ex){
            Logger.getLogger(ProvSearch.class.getName()).log(Level.SEVERE,null,ex);
        }
    }

